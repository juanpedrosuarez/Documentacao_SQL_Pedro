A Função MAX()função retorna o maior valor da coluna selecionada.

SELECT MAX(column_name)
FROM table_name
WHERE condition;

Exemplo:
SELECT MAX(preco)
FROM produtos
  

A Função MIN()função retorna o menor valor da coluna selecionada.

SELECT MIN(column_name)
FROM table_name
WHERE condition;

SELECT MIN(preco)
FROM produtos

