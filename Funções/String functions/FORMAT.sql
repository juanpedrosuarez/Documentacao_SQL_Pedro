A função FORMAT() formata um número para um formato como "#,###,###.##", arredondado
para um número especificado de casas decimais e retorna o resultado como uma string.
  
FORMAT(NUMERO, CASASDECIMAIS)
  
Formate o número como "#,###,###.##" (e arredonde com duas casas decimais):
SELECT FORMAT(250500.5634, 2);
